// Package ts implements the `sov gen ts` subcommand. It hits a running
// gateway's /rpc/_introspect, walks the catalog, and emits a single
// TypeScript file: error union + runtime SovClient + one interface per
// named Go type + one class per router with one typed method per RPC.
package ts

import (
	"fmt"
	"io"
	"sort"
	"strings"

	"github.com/Toyz/sov/cmd/sov/internal/catalog"
	"github.com/Toyz/sov/cmd/sov/internal/docgen"
	"github.com/Toyz/sov/cmd/sov/internal/strs"
	"github.com/Toyz/sov/gateway"
	"github.com/Toyz/sov/rpc"
)

// Run executes the ts subcommand with the supplied argv (everything
// after `sov gen ts`). Returns the exit code; never calls os.Exit so
// tests can drive it. Thin wrapper over catalog.RunEmitter; the only
// ts-specific extras are the drift-warning loop (via PreEmit) and Emit.
func Run(argv []string, stdout, stderr io.Writer) int {
	return catalog.RunEmitter(catalog.EmitterSpec{
		Name:           "sovgen ts",
		DefaultOut:     "./client.ts",
		OutHelp:        "output .ts file; pass \"-\" for stdout",
		DefaultPackage: "sov",
		PackageHelp:    "top-level namespace name",
		PreEmit:        warnDrift,
		Emit:           Emit,
	}, argv, stdout, stderr)
}

// warnDrift logs one line per cross-ref type whose shape diverges
// across services. The generated client uses the first-alphabetical
// variant; this surfaces the divergence to the operator on stderr.
func warnDrift(report *gateway.IntrospectReport, stderr io.Writer) {
	for name, variants := range report.CrossRefs {
		services := []string{}
		for _, v := range variants.Variants {
			services = append(services, v.Services...)
		}
		sort.Strings(services)
		fmt.Fprintf(stderr, "sovgen: drift detected — type %q has %d variants across services %v; client uses the first-alphabetical variant\n",
			name, len(variants.Variants), services)
	}
}

// Emit writes the TypeScript client for report under namespace pkg to w.
// Exposed so `sov gen all` can drive all five emitters from one fetch
// without shelling out to subcommands.
func Emit(w io.Writer, pkg string, report *gateway.IntrospectReport) {
	fmt.Fprintf(w, "// Generated by sovgen — DO NOT EDIT.\n")
	fmt.Fprintf(w, "// Source: aggregated /rpc/_introspect catalog.\n\n")
	fmt.Fprintf(w, "export namespace %s {\n\n", pkg)
	indent := "  "

	writeBlock(w, indent, preamble)
	fmt.Fprintln(w)

	emitTypes(w, indent, report)
	emitRouters(w, indent, report)

	fmt.Fprintln(w, "}")
}

func emitTypes(w io.Writer, indent string, report *gateway.IntrospectReport) {
	if len(report.Types) == 0 {
		return
	}
	names := make([]string, 0, len(report.Types))
	for n := range report.Types {
		names = append(names, n)
	}
	sort.Strings(names)

	fmt.Fprintf(w, "%s// ---- Types ----------------------------------------------------------------\n\n", indent)
	for _, name := range names {
		td := report.Types[name]
		emitJSDoc(w, indent, td.Name, "", "", "", false)
		fmt.Fprintf(w, "%sexport interface %s {\n", indent, tsIdent(name))
		for _, f := range td.Fields {
			emitFieldJSDoc(w, indent+"  ", f)
			marker := ""
			if f.Omitempty || !f.Required {
				marker = "?"
			}
			fmt.Fprintf(w, "%s  %s%s: %s;\n", indent, f.JSONName, marker, tsTypeOf(f))
		}
		fmt.Fprintf(w, "%s}\n\n", indent)
	}
}

func emitRouters(w io.Writer, indent string, report *gateway.IntrospectReport) {
	if len(report.Services) == 0 {
		return
	}

	services := make([]string, 0, len(report.Services))
	for s := range report.Services {
		services = append(services, s)
	}
	sort.Strings(services)

	// 1. Per-method response aliases. Named so both the router class
	//    method signature AND the Services map can reference the same
	//    shape — narrows result types end-to-end through batch().
	fmt.Fprintf(w, "%s// ---- Method response aliases ---------------------------------------------\n\n", indent)
	for _, svc := range services {
		for _, rd := range report.Services[svc] {
			for _, md := range rd.Methods {
				resp := "unknown"
				if md.ResponseTypeScript != "" && md.ResponseTypeScript != "void" {
					resp = md.ResponseTypeScript
				}
				alias := tsIdent(rd.Router) + strs.Capitalize(md.Method) + "Result"
				fmt.Fprintf(w, "%sexport type %s = %s;\n", indent, alias, resp)
			}
		}
	}
	fmt.Fprintln(w)

	// 2. Services map augmentation. TS merges this with the empty
	//    declaration in the preamble so the typed batch() picks up
	//    every (service, method) pair without consumer config.
	fmt.Fprintf(w, "%s// ---- Services catalog (augments the preamble's Services interface) ------\n\n", indent)
	fmt.Fprintf(w, "%sexport interface Services {\n", indent)
	for _, svc := range services {
		for _, rd := range report.Services[svc] {
			fmt.Fprintf(w, "%s  %s: {\n", indent, tsIdent(rd.Router))
			for _, md := range rd.Methods {
				resultAlias := tsIdent(rd.Router) + strs.Capitalize(md.Method) + "Result"
				if md.HasParams {
					paramAlias := tsIdent(rd.Router) + strs.Capitalize(md.Method) + "Params"
					fmt.Fprintf(w, "%s    %s: { params: %s; result: %s };\n",
						indent, md.Method, paramAlias, resultAlias)
				} else {
					fmt.Fprintf(w, "%s    %s: { params: void; result: %s };\n",
						indent, md.Method, resultAlias)
				}
			}
			fmt.Fprintf(w, "%s  };\n", indent)
		}
	}
	fmt.Fprintf(w, "%s}\n\n", indent)

	// 3. Router classes (use the named result aliases).
	fmt.Fprintf(w, "%s// ---- Routers --------------------------------------------------------------\n\n", indent)
	for _, svc := range services {
		for _, rd := range report.Services[svc] {
			fmt.Fprintf(w, "%sexport class %s {\n", indent, tsIdent(rd.Router))
			fmt.Fprintf(w, "%s  constructor(private c: SovClient) {}\n", indent)
			for _, md := range rd.Methods {
				emitMethod(w, indent+"  ", rd.Router, md)
			}
			fmt.Fprintf(w, "%s}\n\n", indent)
		}
	}

	// 4. Convenience SovClient subclass that exposes each router as a member.
	fmt.Fprintf(w, "%sexport class App extends SovClient {\n", indent)
	for _, svc := range services {
		for _, rd := range report.Services[svc] {
			fmt.Fprintf(w, "%s  readonly %s = new %s(this);\n", indent, tsIdent(rd.Router), tsIdent(rd.Router))
		}
	}
	fmt.Fprintf(w, "%s}\n", indent)
}

func emitMethod(w io.Writer, indent, router string, md rpc.MethodDescriptor) {
	paramType := tsIdent(router) + strs.Capitalize(md.Method) + "Params"
	paramArg := "p: " + paramType
	if !md.HasParams {
		paramArg = ""
	}
	respType := tsIdent(router) + strs.Capitalize(md.Method) + "Result"
	if md.HasParams {
		fmt.Fprintf(w, "%sasync %s(%s): Promise<%s> {\n", indent, md.Method, paramArg, respType)
		fmt.Fprintf(w, "%s  return this.c.call(%q, %q, p);\n", indent, router, md.Method)
	} else {
		fmt.Fprintf(w, "%sasync %s(): Promise<%s> {\n", indent, md.Method, respType)
		fmt.Fprintf(w, "%s  return this.c.call(%q, %q);\n", indent, router, md.Method)
	}
	fmt.Fprintf(w, "%s}\n", indent)
}

// jsDocStyle is the JSDoc block style: /** ... */ with " * " line
// prefix and the @example/@deprecated tags.
var jsDocStyle = docgen.DocStyle{
	Opener:         "/**",
	Closer:         " */",
	LinePrefix:     " * ",
	BlankPrefix:    " *",
	BlankSeparator: true,
	ExampleTag:     "@example ",
	DeprecatedLine: " * @deprecated",
}

func emitJSDoc(w io.Writer, indent, title, desc, doc, example string, deprecated bool) {
	docgen.Render(w, indent, jsDocStyle, title, desc, doc, example, deprecated)
}

func emitFieldJSDoc(w io.Writer, indent string, f rpc.ParamField) {
	docgen.RenderField(w, indent, jsDocStyle, f)
}

func tsTypeOf(f rpc.ParamField) string {
	switch f.SchemaType {
	case "string":
		return "string"
	case "number":
		return "number"
	case "boolean":
		return "boolean"
	case "array":
		return "unknown[]"
	case "object":
		if f.TypeName != "" {
			return tsIdent(f.TypeName)
		}
		return "Record<string, unknown>"
	default:
		return "unknown"
	}
}

func tsIdent(name string) string {
	// Replace dots used in generated type names (Router.MethodParams)
	// with a flat camel-like join — TS interface names can't contain dots.
	return strings.ReplaceAll(name, ".", "")
}

func writeBlock(w io.Writer, indent, text string) {
	for _, line := range strings.Split(strings.TrimRight(text, "\n"), "\n") {
		if line == "" {
			fmt.Fprintln(w)
			continue
		}
		fmt.Fprintf(w, "%s%s\n", indent, line)
	}
}
