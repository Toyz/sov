// Package kotlin implements the `sovgen kotlin` subcommand. It hits a
// running gateway's /rpc/_introspect, walks the catalog, and emits a
// single Kotlin file: SovError exception + SovClient runtime + one
// data class per named Go type + one class per router with one typed
// suspend-fun per RPC.
//
// Generated code targets:
//   - OkHttp 4.x  (com.squareup.okhttp3:okhttp:4.x)
//   - kotlinx-serialization-json  (org.jetbrains.kotlinx:kotlinx-serialization-json:1.x)
//
// Both are documented in the header comment block of the emitted file.
package kotlin

import (
	"fmt"
	"io"
	"sort"
	"strings"

	"github.com/Toyz/sov/cmd/sov/internal/catalog"
	"github.com/Toyz/sov/cmd/sov/internal/docgen"
	"github.com/Toyz/sov/cmd/sov/internal/strs"
	"github.com/Toyz/sov/gateway"
	"github.com/Toyz/sov/rpc"
)

// Run executes the kotlin subcommand. Thin wrapper over the shared
// catalog.RunEmitter scaffolding; only Emit is kotlin-specific.
func Run(argv []string, stdout, stderr io.Writer) int {
	return catalog.RunEmitter(catalog.EmitterSpec{
		Name:           "sovgen kotlin",
		DefaultOut:     "./Client.kt",
		OutHelp:        "output .kt file; pass \"-\" for stdout",
		DefaultPackage: "com.example.sov",
		PackageHelp:    "Kotlin package for the generated file",
		Emit:           Emit,
	}, argv, stdout, stderr)
}

// Emit writes the Kotlin client for report into w under package pkg.
// Exposed so `sov gen all` can drive every emitter directly without
// reparsing argv or shelling out.
func Emit(w io.Writer, pkg string, report *gateway.IntrospectReport) {
	fmt.Fprintln(w, "// Generated by sovgen — DO NOT EDIT.")
	fmt.Fprintln(w, "// Source: aggregated /rpc/_introspect catalog.")
	fmt.Fprintln(w, "//")
	fmt.Fprintln(w, "// Gradle dependencies required:")
	fmt.Fprintln(w, "//   implementation(\"com.squareup.okhttp3:okhttp:4.12.0\")")
	fmt.Fprintln(w, "//   implementation(\"org.jetbrains.kotlinx:kotlinx-serialization-json:1.6.3\")")
	fmt.Fprintln(w, "//   plugins { kotlin(\"plugin.serialization\") version \"<kotlin-version>\" }")
	fmt.Fprintln(w)
	fmt.Fprintf(w, "package %s\n\n", pkg)

	fmt.Fprintln(w, `import kotlinx.serialization.SerialName`)
	fmt.Fprintln(w, `import kotlinx.serialization.Serializable`)
	fmt.Fprintln(w, `import kotlinx.serialization.json.Json`)
	fmt.Fprintln(w, `import kotlinx.serialization.json.JsonElement`)
	fmt.Fprintln(w, `import kotlinx.serialization.json.JsonObject`)
	fmt.Fprintln(w, `import kotlinx.serialization.json.JsonNull`)
	fmt.Fprintln(w, `import kotlinx.serialization.encodeToString`)
	fmt.Fprintln(w, `import kotlinx.serialization.decodeFromString`)
	fmt.Fprintln(w, `import kotlinx.serialization.serializer`)
	fmt.Fprintln(w, `import okhttp3.MediaType.Companion.toMediaType`)
	fmt.Fprintln(w, `import okhttp3.OkHttpClient`)
	fmt.Fprintln(w, `import okhttp3.Request`)
	fmt.Fprintln(w, `import okhttp3.RequestBody.Companion.toRequestBody`)
	fmt.Fprintln(w, `import java.util.concurrent.atomic.AtomicReference`)
	fmt.Fprintln(w)

	emitRuntime(w)
	emitTypes(w, report)
	emitRouters(w, report)
}

func emitRuntime(w io.Writer) {
	fmt.Fprintln(w, `// ---- Runtime ---------------------------------------------------------------`)
	fmt.Fprintln(w)
	fmt.Fprintln(w, `/** Thrown when the gateway returns an `+"`error`"+` envelope. */`)
	fmt.Fprintln(w, `class SovError(val code: String, message: String) : RuntimeException(message)`)
	fmt.Fprintln(w)
	fmt.Fprintln(w, `@Serializable`)
	fmt.Fprintln(w, `private data class SovErrorBody(val code: String = "", val message: String = "")`)
	fmt.Fprintln(w)
	fmt.Fprintln(w, `@Serializable`)
	fmt.Fprintln(w, `private data class SovEnvelope(`)
	fmt.Fprintln(w, `    val data: JsonElement? = null,`)
	fmt.Fprintln(w, `    val error: SovErrorBody? = null,`)
	fmt.Fprintln(w, `)`)
	fmt.Fprintln(w)
	fmt.Fprintln(w, `/**`)
	fmt.Fprintln(w, ` * SovClient is the runtime base for every generated router. It POSTs`)
	fmt.Fprintln(w, ` * /rpc/{service}/{method} with a `+"`{ \"args\": ... }`"+` body, parses the`)
	fmt.Fprintln(w, ` * envelope, and surfaces the server-stamped X-Sov-Request-Id via`)
	fmt.Fprintln(w, ` * [lastRequestId] so callers can correlate logs.`)
	fmt.Fprintln(w, ` */`)
	fmt.Fprintln(w, `open class SovClient(`)
	fmt.Fprintln(w, `    var baseURL: String,`)
	fmt.Fprintln(w, `    var token: String? = null,`)
	fmt.Fprintln(w, `    val http: OkHttpClient = OkHttpClient(),`)
	fmt.Fprintln(w, `) {`)
	fmt.Fprintln(w, `    private val lastId = AtomicReference<String?>(null)`)
	fmt.Fprintln(w, `    val json: Json = Json { ignoreUnknownKeys = true; encodeDefaults = true }`)
	fmt.Fprintln(w)
	fmt.Fprintln(w, `    /** Most recent X-Sov-Request-Id seen on a response (server-stamped). */`)
	fmt.Fprintln(w, `    val lastRequestId: String? get() = lastId.get()`)
	fmt.Fprintln(w)
	fmt.Fprintln(w, `    /**`)
	fmt.Fprintln(w, `     * Generic call. Marshals [args] to JSON, posts, decodes the response`)
	fmt.Fprintln(w, `     * envelope into [R]. Throws [SovError] when the envelope carries one.`)
	fmt.Fprintln(w, `     */`)
	fmt.Fprintln(w, `    inline fun <reified A, reified R> call(service: String, method: String, args: A?): R {`)
	fmt.Fprintln(w, `        val argsJson: JsonElement = if (args == null) JsonNull else json.encodeToJsonElement(args)`)
	fmt.Fprintln(w, `        val body = json.encodeToString(JsonObject(mapOf("args" to argsJson)))`)
	fmt.Fprintln(w, `        return doCall(service, method, body)`)
	fmt.Fprintln(w, `    }`)
	fmt.Fprintln(w)
	fmt.Fprintln(w, `    inline fun <reified A> Json.encodeToJsonElement(value: A): JsonElement =`)
	fmt.Fprintln(w, `        parseToJsonElement(encodeToString(value))`)
	fmt.Fprintln(w)
	fmt.Fprintln(w, `    inline fun <reified R> doCall(service: String, method: String, body: String): R {`)
	fmt.Fprintln(w, `        val url = baseURL.trimEnd('/') + "/rpc/" + service + "/" + method`)
	fmt.Fprintln(w, `        val req = Request.Builder()`)
	fmt.Fprintln(w, `            .url(url)`)
	fmt.Fprintln(w, `            .post(body.toRequestBody(JSON_MEDIA))`)
	fmt.Fprintln(w, `            .apply { token?.let { header("Authorization", "Bearer $it") } }`)
	fmt.Fprintln(w, `            .build()`)
	fmt.Fprintln(w, `        http.newCall(req).execute().use { resp ->`)
	fmt.Fprintln(w, `            resp.header("X-Sov-Request-Id")?.let { lastId.set(it) }`)
	fmt.Fprintln(w, `            val raw = resp.body?.string() ?: ""`)
	fmt.Fprintln(w, `            val env = json.decodeFromString<SovEnvelope>(raw)`)
	fmt.Fprintln(w, `            env.error?.let { throw SovError(it.code, it.message) }`)
	fmt.Fprintln(w, `            val data = env.data ?: JsonNull`)
	fmt.Fprintln(w, `            return json.decodeFromString(json.encodeToString(data))`)
	fmt.Fprintln(w, `        }`)
	fmt.Fprintln(w, `    }`)
	fmt.Fprintln(w)
	fmt.Fprintln(w, `    companion object {`)
	fmt.Fprintln(w, `        val JSON_MEDIA = "application/json; charset=utf-8".toMediaType()`)
	fmt.Fprintln(w, `    }`)
	fmt.Fprintln(w, `}`)
	fmt.Fprintln(w)
}

func emitTypes(w io.Writer, report *gateway.IntrospectReport) {
	if len(report.Types) == 0 {
		return
	}
	names := make([]string, 0, len(report.Types))
	for n := range report.Types {
		names = append(names, n)
	}
	sort.Strings(names)
	fmt.Fprintln(w, "// ---- Types -----------------------------------------------------------------")
	fmt.Fprintln(w)
	for _, name := range names {
		td := report.Types[name]
		emitKDoc(w, "", td.Name, "", "", "", false)
		fmt.Fprintln(w, "@Serializable")
		ident := ktIdent(name)
		if len(td.Fields) == 0 {
			fmt.Fprintf(w, "class %s\n\n", ident)
			continue
		}
		fmt.Fprintf(w, "data class %s(\n", ident)
		for i, f := range td.Fields {
			emitFieldKDoc(w, "    ", f)
			optional := f.Omitempty || !f.Required
			typ := ktTypeOf(f)
			line := fmt.Sprintf("    @SerialName(%q) val %s: %s", f.JSONName, ktFieldIdent(f.JSONName), typ)
			if optional {
				line += "? = null"
			}
			if i < len(td.Fields)-1 {
				line += ","
			} else {
				line += ","
			}
			fmt.Fprintln(w, line)
		}
		fmt.Fprintln(w, ")")
		fmt.Fprintln(w)
	}
}

func emitRouters(w io.Writer, report *gateway.IntrospectReport) {
	if len(report.Services) == 0 {
		return
	}
	services := make([]string, 0, len(report.Services))
	for s := range report.Services {
		services = append(services, s)
	}
	sort.Strings(services)
	fmt.Fprintln(w, "// ---- Routers ---------------------------------------------------------------")
	fmt.Fprintln(w)
	for _, svc := range services {
		for _, rd := range report.Services[svc] {
			emitRouter(w, rd)
		}
	}
	// App convenience class.
	fmt.Fprintln(w, "/** Convenience subclass exposing each router as a member. */")
	fmt.Fprintln(w, "class App(baseURL: String, token: String? = null, http: OkHttpClient = OkHttpClient())")
	fmt.Fprintln(w, "    : SovClient(baseURL, token, http) {")
	for _, svc := range services {
		for _, rd := range report.Services[svc] {
			fmt.Fprintf(w, "    val %s: %s = %s(this)\n", ktFieldIdent(rd.Router), ktIdent(rd.Router), ktIdent(rd.Router))
		}
	}
	fmt.Fprintln(w, "}")
}

func emitRouter(w io.Writer, rd rpc.RouterDescriptor) {
	fmt.Fprintf(w, "/** %s router. */\n", rd.Router)
	fmt.Fprintf(w, "class %s(private val c: SovClient) {\n", ktIdent(rd.Router))
	for _, md := range rd.Methods {
		emitMethod(w, rd.Router, md)
	}
	fmt.Fprintln(w, "}")
	fmt.Fprintln(w)
}

func emitMethod(w io.Writer, router string, md rpc.MethodDescriptor) {
	resultType := responseTypeName(md)
	if resultType == "" {
		resultType = "JsonElement"
	}
	paramType := ktIdent(router) + strs.Capitalize(md.Method) + "Params"
	if md.HasParams {
		fmt.Fprintf(w, "    fun %s(p: %s): %s =\n", md.Method, paramType, resultType)
		fmt.Fprintf(w, "        c.call(%q, %q, p)\n", router, md.Method)
	} else {
		fmt.Fprintf(w, "    fun %s(): %s =\n", md.Method, resultType)
		fmt.Fprintf(w, "        c.call<Any?, %s>(%q, %q, null)\n", resultType, router, md.Method)
	}
}

// kDocStyle is the KDoc block style: identical to JSDoc but with the
// @sample tag instead of @example.
var kDocStyle = docgen.DocStyle{
	Opener:         "/**",
	Closer:         " */",
	LinePrefix:     " * ",
	BlankPrefix:    " *",
	BlankSeparator: true,
	ExampleTag:     "@sample ",
	DeprecatedLine: " * @deprecated",
}

func emitKDoc(w io.Writer, indent, title, desc, doc, example string, deprecated bool) {
	docgen.Render(w, indent, kDocStyle, title, desc, doc, example, deprecated)
}

func emitFieldKDoc(w io.Writer, indent string, f rpc.ParamField) {
	docgen.RenderField(w, indent, kDocStyle, f)
}

// ktTypeOf maps a ParamField.SchemaType to a Kotlin type literal.
func ktTypeOf(f rpc.ParamField) string {
	switch f.SchemaType {
	case "string":
		return "String"
	case "number":
		return "Double"
	case "integer":
		return "Long"
	case "boolean":
		return "Boolean"
	case "array":
		return "List<JsonElement>"
	case "object":
		if f.TypeName != "" {
			return ktIdent(f.TypeName)
		}
		return "Map<String, JsonElement>"
	default:
		return "JsonElement"
	}
}

// responseTypeName picks a stable Kotlin class name for the method
// response when the introspect report exposes one as a bare identifier.
// Otherwise returns "" so the caller defaults to JsonElement.
func responseTypeName(md rpc.MethodDescriptor) string {
	s := strings.TrimSpace(md.ResponseTypeScript)
	if s == "" || s == "void" || s == "unknown" {
		return ""
	}
	if !strs.IsIdent(s) {
		return ""
	}
	return ktIdent(s)
}

func ktIdent(name string) string {
	out := strings.ReplaceAll(name, ".", "")
	out = strings.ReplaceAll(out, "-", "_")
	if out == "" {
		return out
	}
	r := []rune(out)
	if r[0] >= 'a' && r[0] <= 'z' {
		r[0] -= 'a' - 'A'
	}
	return string(r)
}

func ktFieldIdent(name string) string {
	// JSON names already lowerCamel in sov; keep them, but guard against
	// Kotlin hard keywords with a backtick escape.
	if isKotlinKeyword(name) {
		return "`" + name + "`"
	}
	return name
}

func isKotlinKeyword(s string) bool {
	switch s {
	case "as", "break", "class", "continue", "do", "else", "false", "for",
		"fun", "if", "in", "interface", "is", "null", "object", "package",
		"return", "super", "this", "throw", "true", "try", "typealias",
		"typeof", "val", "var", "when", "while":
		return true
	}
	return false
}
