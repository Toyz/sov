// Package swift implements the `sovgen swift` subcommand. It hits a
// running gateway's /rpc/_introspect, walks the catalog, and emits a
// single Swift file: SovError + SovClient runtime + one Codable
// struct per named Go type + one class per router with one typed
// async method per RPC.
//
// Generated code targets the standard library only (URLSession,
// JSONEncoder/Decoder). No external dependencies.
package swift

import (
	"fmt"
	"io"
	"sort"
	"strings"

	"github.com/Toyz/sov/cmd/sov/internal/catalog"
	"github.com/Toyz/sov/cmd/sov/internal/docgen"
	"github.com/Toyz/sov/cmd/sov/internal/strs"
	"github.com/Toyz/sov/gateway"
	"github.com/Toyz/sov/rpc"
)

// Run executes the swift subcommand. Thin wrapper over the shared
// catalog.RunEmitter scaffolding; only Emit is swift-specific.
func Run(argv []string, stdout, stderr io.Writer) int {
	return catalog.RunEmitter(catalog.EmitterSpec{
		Name:           "sovgen swift",
		DefaultOut:     "./Client.swift",
		OutHelp:        "output .swift file; pass \"-\" for stdout",
		DefaultPackage: "Sov",
		PackageHelp:    "Swift namespace enum name (also doubles as the generated module label)",
		Emit:           Emit,
	}, argv, stdout, stderr)
}

// Emit writes the Swift client for report into w under namespace pkg.
// Exposed so `sov gen all` can drive every emitter directly without
// reparsing argv or shelling out.
func Emit(w io.Writer, pkg string, report *gateway.IntrospectReport) {
	fmt.Fprintln(w, "// Generated by sovgen — DO NOT EDIT.")
	fmt.Fprintln(w, "// Source: aggregated /rpc/_introspect catalog.")
	fmt.Fprintln(w, "//")
	fmt.Fprintln(w, "// Dependencies: Foundation (URLSession, JSONEncoder/Decoder). No extra packages.")
	fmt.Fprintln(w, "// Requires Swift 5.5+ for async/await.")
	fmt.Fprintln(w)
	fmt.Fprintln(w, "import Foundation")
	fmt.Fprintln(w)
	fmt.Fprintf(w, "public enum %s {\n", swIdent(pkg))
	fmt.Fprintln(w)

	// Router class names — a generated TYPE that collides with one gets a
	// "Model" suffix (see swTypeIdent) so a data struct never collides with
	// a router class of the same name.
	routers := map[string]bool{}
	for svc := range report.Services {
		for _, rd := range report.Services[svc] {
			routers[swIdent(rd.Router)] = true
		}
	}

	emitRuntime(w)
	emitTypes(w, report, routers)
	emitRouters(w, report)

	fmt.Fprintln(w, "}")
}

func emitRuntime(w io.Writer) {
	fmt.Fprintln(w, "    // ---- Runtime -----------------------------------------------------------")
	fmt.Fprintln(w)
	fmt.Fprintln(w, `    /// Thrown when the gateway returns an `+"`"+`error`+"`"+` envelope.`)
	fmt.Fprintln(w, "    public struct SovError: Error, Codable {")
	fmt.Fprintln(w, "        public let code: String")
	fmt.Fprintln(w, "        public let message: String")
	fmt.Fprintln(w, "    }")
	fmt.Fprintln(w)
	fmt.Fprintln(w, "    private struct Envelope<T: Decodable>: Decodable {")
	fmt.Fprintln(w, "        let data: T?")
	fmt.Fprintln(w, "        let error: SovError?")
	fmt.Fprintln(w, "    }")
	fmt.Fprintln(w)
	fmt.Fprintln(w, "    private struct ArgsBody<A: Encodable>: Encodable {")
	fmt.Fprintln(w, "        let args: A")
	fmt.Fprintln(w, "    }")
	fmt.Fprintln(w)
	fmt.Fprintln(w, "    /// Empty parameter marker for argument-less RPCs.")
	fmt.Fprintln(w, "    public struct Empty: Codable { public init() {} }")
	fmt.Fprintln(w)
	fmt.Fprintln(w, `    /// SovClient is the runtime base for every generated router. It POSTs`)
	fmt.Fprintln(w, `    /// /rpc/{service}/{method} with a `+"`"+`{ "args": ... }`+"`"+` body, parses the`)
	fmt.Fprintln(w, `    /// envelope, and surfaces the server-stamped X-Sov-Request-Id via`)
	fmt.Fprintln(w, `    /// [lastRequestID] so callers can correlate logs.`)
	fmt.Fprintln(w, "    public class SovClient {")
	fmt.Fprintln(w, "        public var baseURL: URL")
	fmt.Fprintln(w, "        public var token: String?")
	fmt.Fprintln(w, "        public let session: URLSession")
	fmt.Fprintln(w, "        public let encoder: JSONEncoder")
	fmt.Fprintln(w, "        public let decoder: JSONDecoder")
	fmt.Fprintln(w)
	fmt.Fprintln(w, "        private let lock = NSLock()")
	fmt.Fprintln(w, "        private var _lastRequestID: String?")
	fmt.Fprintln(w, "        public var lastRequestID: String? {")
	fmt.Fprintln(w, "            lock.lock(); defer { lock.unlock() }")
	fmt.Fprintln(w, "            return _lastRequestID")
	fmt.Fprintln(w, "        }")
	fmt.Fprintln(w)
	fmt.Fprintln(w, "        public init(baseURL: URL, token: String? = nil, session: URLSession = .shared) {")
	fmt.Fprintln(w, "            self.baseURL = baseURL")
	fmt.Fprintln(w, "            self.token = token")
	fmt.Fprintln(w, "            self.session = session")
	fmt.Fprintln(w, "            self.encoder = JSONEncoder()")
	fmt.Fprintln(w, "            self.decoder = JSONDecoder()")
	fmt.Fprintln(w, "        }")
	fmt.Fprintln(w)
	fmt.Fprintln(w, "        /// Generic call. Encodes [args], posts, decodes the response envelope")
	fmt.Fprintln(w, "        /// into [R]. Throws [SovError] when the envelope carries one.")
	fmt.Fprintln(w, "        public func call<A: Encodable, R: Decodable>(_ service: String, _ method: String, args: A) async throws -> R {")
	fmt.Fprintln(w, "            let body = try encoder.encode(ArgsBody(args: args))")
	fmt.Fprintln(w, "            var req = URLRequest(url: baseURL.appendingPathComponent(\"rpc/\\(service)/\\(method)\"))")
	fmt.Fprintln(w, "            req.httpMethod = \"POST\"")
	fmt.Fprintln(w, "            req.setValue(\"application/json\", forHTTPHeaderField: \"Content-Type\")")
	fmt.Fprintln(w, "            if let token = token {")
	fmt.Fprintln(w, "                req.setValue(\"Bearer \\(token)\", forHTTPHeaderField: \"Authorization\")")
	fmt.Fprintln(w, "            }")
	fmt.Fprintln(w, "            req.httpBody = body")
	fmt.Fprintln(w, "            let (data, resp) = try await session.data(for: req)")
	fmt.Fprintln(w, "            if let http = resp as? HTTPURLResponse,")
	fmt.Fprintln(w, "               let id = http.value(forHTTPHeaderField: \"X-Sov-Request-Id\"), !id.isEmpty {")
	fmt.Fprintln(w, "                lock.lock(); _lastRequestID = id; lock.unlock()")
	fmt.Fprintln(w, "            }")
	fmt.Fprintln(w, "            let env = try decoder.decode(Envelope<R>.self, from: data)")
	fmt.Fprintln(w, "            if let err = env.error { throw err }")
	fmt.Fprintln(w, "            guard let out = env.data else {")
	fmt.Fprintln(w, "                throw SovError(code: \"sov.decode\", message: \"missing data field in envelope\")")
	fmt.Fprintln(w, "            }")
	fmt.Fprintln(w, "            return out")
	fmt.Fprintln(w, "        }")
	fmt.Fprintln(w, "    }")
	fmt.Fprintln(w)
}

func emitTypes(w io.Writer, report *gateway.IntrospectReport, routers map[string]bool) {
	if len(report.Types) == 0 {
		return
	}
	names := make([]string, 0, len(report.Types))
	for n := range report.Types {
		names = append(names, n)
	}
	sort.Strings(names)
	fmt.Fprintln(w, "    // ---- Types -------------------------------------------------------------")
	fmt.Fprintln(w)
	for _, name := range names {
		td := report.Types[name]
		emitDocComment(w, "    ", td.Name, "", "", "", false)
		fmt.Fprintf(w, "    public struct %s: Codable {\n", swTypeIdent(name, routers))
		// Field declarations.
		for _, f := range td.Fields {
			emitFieldDoc(w, "        ", f)
			// Presence-based optionality: a field is optional (`T?`) iff it
			// can be absent on the wire — omitempty (may be dropped) or a
			// pointer (may be null/absent). Required is sov VALIDATION only
			// and does NOT drive wire-presence, so it is not consulted here.
			optional := f.Omitempty || f.Nullable
			typ := swTypeOf(f, routers)
			if optional {
				typ += "?"
			}
			fmt.Fprintf(w, "        public var %s: %s\n", swFieldIdent(f.JSONName), typ)
		}
		// CodingKeys (in case JSON names disagree with Swift identifiers).
		if len(td.Fields) > 0 {
			fmt.Fprintln(w, "        private enum CodingKeys: String, CodingKey {")
			for _, f := range td.Fields {
				fmt.Fprintf(w, "            case %s = %q\n", swFieldIdent(f.JSONName), f.JSONName)
			}
			fmt.Fprintln(w, "        }")
		}
		// Memberwise public init.
		if len(td.Fields) > 0 {
			fmt.Fprint(w, "        public init(")
			for i, f := range td.Fields {
				optional := f.Omitempty || f.Nullable
				typ := swTypeOf(f, routers)
				if optional {
					typ += "?"
				}
				if i > 0 {
					fmt.Fprint(w, ", ")
				}
				if optional {
					fmt.Fprintf(w, "%s: %s = nil", swFieldIdent(f.JSONName), typ)
				} else {
					fmt.Fprintf(w, "%s: %s", swFieldIdent(f.JSONName), typ)
				}
			}
			fmt.Fprintln(w, ") {")
			for _, f := range td.Fields {
				fmt.Fprintf(w, "            self.%s = %s\n", swFieldIdent(f.JSONName), swFieldIdent(f.JSONName))
			}
			fmt.Fprintln(w, "        }")
		}
		fmt.Fprintln(w, "    }")
		fmt.Fprintln(w)
	}
}

func emitRouters(w io.Writer, report *gateway.IntrospectReport) {
	if len(report.Services) == 0 {
		return
	}
	services := make([]string, 0, len(report.Services))
	for s := range report.Services {
		services = append(services, s)
	}
	sort.Strings(services)
	fmt.Fprintln(w, "    // ---- Routers -----------------------------------------------------------")
	fmt.Fprintln(w)
	for _, svc := range services {
		for _, rd := range report.Services[svc] {
			emitRouter(w, rd)
		}
	}
	fmt.Fprintln(w, "    /// Convenience subclass exposing each router as a property.")
	fmt.Fprintln(w, "    public final class App: SovClient {")
	for _, svc := range services {
		for _, rd := range report.Services[svc] {
			fmt.Fprintf(w, "        public lazy var %s: %s = %s(client: self)\n",
				swFieldIdent(rd.Router), swIdent(rd.Router), swIdent(rd.Router))
		}
	}
	fmt.Fprintln(w, "    }")
}

func emitRouter(w io.Writer, rd rpc.RouterDescriptor) {
	fmt.Fprintf(w, "    /// %s router.\n", rd.Router)
	fmt.Fprintf(w, "    public final class %s {\n", swIdent(rd.Router))
	fmt.Fprintln(w, "        public let client: SovClient")
	fmt.Fprintln(w, "        public init(client: SovClient) { self.client = client }")
	for _, md := range rd.Methods {
		emitMethod(w, rd.Router, md)
	}
	fmt.Fprintln(w, "    }")
	fmt.Fprintln(w)
}

// methodHasParams reports whether the client must send a params struct.
// It double-checks len(Params) > 0 rather than trusting HasParams alone:
// a catalog that reports HasParams=true but ships zero param fields would
// otherwise make the generator reference a {Router}{Method}Params struct
// that emitTypes never emits — a dangling reference that won't compile.
// Zero fields → no-arg method posting Empty().
func methodHasParams(md rpc.MethodDescriptor) bool {
	return md.HasParams && len(md.Params) > 0
}

func emitMethod(w io.Writer, router string, md rpc.MethodDescriptor) {
	respType := responseTypeName(md)
	resultDecl := respType
	resultDecodeType := respType
	if respType == "" {
		// Fallback to a dynamic JSON value; AnyCodable would be ideal but
		// we stay stdlib-only — decode into a permissive dictionary.
		resultDecl = "[String: String]"
		resultDecodeType = "[String: String]"
	}
	paramType := swIdent(router) + strs.Capitalize(md.Method) + "Params"
	if methodHasParams(md) {
		fmt.Fprintf(w, "        public func %s(_ p: %s) async throws -> %s {\n", md.Method, paramType, resultDecl)
		fmt.Fprintf(w, "            try await client.call(%q, %q, args: p) as %s\n", router, md.Method, resultDecodeType)
		fmt.Fprintln(w, "        }")
	} else {
		fmt.Fprintf(w, "        public func %s() async throws -> %s {\n", md.Method, resultDecl)
		fmt.Fprintf(w, "            try await client.call(%q, %q, args: Empty()) as %s\n", router, md.Method, resultDecodeType)
		fmt.Fprintln(w, "        }")
	}
}

// swDocStyle is Swift's per-line "///" doc style: no block opener/
// closer, no blank separator before the doc body, "Example:" prefix,
// and the @available deprecation attribute.
var swDocStyle = docgen.DocStyle{
	LinePrefix:     "/// ",
	ExampleTag:     "Example: ",
	DeprecatedLine: "@available(*, deprecated)",
}

func emitDocComment(w io.Writer, indent, title, desc, doc, example string, deprecated bool) {
	docgen.Render(w, indent, swDocStyle, title, desc, doc, example, deprecated)
}

func emitFieldDoc(w io.Writer, indent string, f rpc.ParamField) {
	docgen.RenderField(w, indent, swDocStyle, f)
}

func swTypeOf(f rpc.ParamField, routers map[string]bool) string {
	switch f.SchemaType {
	case "string":
		return "String"
	case "number":
		return "Double"
	case "integer":
		return "Int64"
	case "boolean":
		return "Bool"
	case "array":
		return "[" + swArrayElem(f, routers) + "]"
	case "object":
		if f.TypeName != "" {
			return swTypeIdent(f.TypeName, routers)
		}
		return "[String: String]"
	default:
		return "String"
	}
}

// swArrayElem returns the Swift type of a slice element. A named struct
// element (TypeName set) → the type ident; otherwise the element's scalar
// schema (ElemType) maps to the SAME Swift scalars swTypeOf uses for
// string/number/boolean/object; anything else falls back to String so the
// file still compiles.
func swArrayElem(f rpc.ParamField, routers map[string]bool) string {
	if f.TypeName != "" {
		return swTypeIdent(f.TypeName, routers)
	}
	switch f.ElemType {
	case "string":
		return "String"
	case "number":
		return "Double"
	case "integer":
		return "Int64"
	case "boolean":
		return "Bool"
	case "object":
		return "[String: String]"
	default:
		return "String"
	}
}

func responseTypeName(md rpc.MethodDescriptor) string {
	s := strings.TrimSpace(md.ResponseTypeScript)
	if s == "" || s == "void" || s == "unknown" {
		return ""
	}
	if !strs.IsIdent(s) {
		return ""
	}
	return swIdent(s)
}

func swIdent(name string) string {
	out := strings.ReplaceAll(name, ".", "")
	out = strings.ReplaceAll(out, "-", "_")
	if out == "" {
		return out
	}
	r := []rune(out)
	if r[0] >= 'a' && r[0] <= 'z' {
		r[0] -= 'a' - 'A'
	}
	return string(r)
}

// swTypeIdent maps a catalog TYPE name to its Swift identifier, suffixing
// "Model" when it collides with a router CLASS name. Without this a data
// struct and a router class sharing a name (e.g. a `Page` model + a `Page`
// router) would both declare `struct/class Page` in the same enum scope —
// a redeclaration that won't compile.
func swTypeIdent(name string, routers map[string]bool) string {
	id := swIdent(name)
	if routers[id] {
		return id + "Model"
	}
	return id
}

func swFieldIdent(name string) string {
	if isSwiftKeyword(name) {
		return "`" + name + "`"
	}
	return name
}

func isSwiftKeyword(s string) bool {
	switch s {
	case "associatedtype", "class", "deinit", "enum", "extension", "fileprivate",
		"func", "import", "init", "inout", "internal", "let", "open", "operator",
		"private", "protocol", "public", "rethrows", "static", "struct", "subscript",
		"typealias", "var", "break", "case", "continue", "default", "defer", "do",
		"else", "fallthrough", "for", "guard", "if", "in", "repeat", "return",
		"switch", "where", "while", "as", "Any", "catch", "false", "is", "nil",
		"super", "self", "Self", "throw", "throws", "true", "try":
		return true
	}
	return false
}
