// Package python implements the `sovgen python` subcommand. It hits a
// running gateway's /rpc/_introspect, walks the catalog, and emits a
// single Python file: SovError + SovClient runtime + one
// @dataclass per named Go type + one class per router with one typed
// method per RPC.
//
// Generated code targets:
//   - httpx (pip install httpx)  — sync client used by SovClient
//   - dataclasses (stdlib)        — request/response shapes
//   - typing (stdlib)             — Optional/Any
package python

import (
	"fmt"
	"io"
	"sort"
	"strings"

	"github.com/Toyz/sov/cmd/sov/internal/catalog"
	"github.com/Toyz/sov/cmd/sov/internal/strs"
	"github.com/Toyz/sov/gateway"
	"github.com/Toyz/sov/rpc"
)

// Run executes the python subcommand. Thin wrapper over the shared
// catalog.RunEmitter scaffolding; only Emit is python-specific.
func Run(argv []string, stdout, stderr io.Writer) int {
	return catalog.RunEmitter(catalog.EmitterSpec{
		Name:           "sovgen python",
		DefaultOut:     "./client.py",
		OutHelp:        "output .py file; pass \"-\" for stdout",
		DefaultPackage: "sovclient",
		PackageHelp:    "Python module label (used in the header docstring)",
		Emit:           Emit,
	}, argv, stdout, stderr)
}

// Emit writes the Python client for report into w using pkg as the
// module label in the header docstring. Exposed so `sov gen all` can
// drive every emitter directly without reparsing argv or shelling out.
func Emit(w io.Writer, pkg string, report *gateway.IntrospectReport) {
	fmt.Fprintln(w, `"""`)
	fmt.Fprintf(w, "%s — generated by sovgen. DO NOT EDIT.\n", pkg)
	fmt.Fprintln(w, "Source: aggregated /rpc/_introspect catalog.")
	fmt.Fprintln(w)
	fmt.Fprintln(w, "Dependencies:")
	fmt.Fprintln(w, "  pip install httpx")
	fmt.Fprintln(w, `"""`)
	fmt.Fprintln(w)
	fmt.Fprintln(w, "from __future__ import annotations")
	fmt.Fprintln(w)
	fmt.Fprintln(w, "from dataclasses import dataclass, field, asdict")
	fmt.Fprintln(w, "from typing import Any, Dict, List, Optional")
	fmt.Fprintln(w, "import threading")
	fmt.Fprintln(w, "import httpx")
	fmt.Fprintln(w)

	emitRuntime(w)
	emitTypes(w, report)
	emitRouters(w, report)
}

func emitRuntime(w io.Writer) {
	fmt.Fprintln(w, "# ---- Runtime ----------------------------------------------------------------")
	fmt.Fprintln(w)
	fmt.Fprintln(w, "class SovError(Exception):")
	fmt.Fprintln(w, `    """Raised when the gateway returns an error envelope."""`)
	fmt.Fprintln(w, "    def __init__(self, code: str, message: str):")
	fmt.Fprintln(w, "        super().__init__(message)")
	fmt.Fprintln(w, "        self.code = code")
	fmt.Fprintln(w, "        self.message = message")
	fmt.Fprintln(w)
	fmt.Fprintln(w, "class SovClient:")
	fmt.Fprintln(w, `    """SovClient is the runtime base for every generated router. POSTs`)
	fmt.Fprintln(w, `    /rpc/{service}/{method} with a {"args": ...} body, parses the`)
	fmt.Fprintln(w, `    envelope, and surfaces the server-stamped X-Sov-Request-Id via`)
	fmt.Fprintln(w, `    `+"`"+`last_request_id`+"`"+` so callers can correlate logs.`)
	fmt.Fprintln(w, `    """`)
	fmt.Fprintln(w)
	fmt.Fprintln(w, "    def __init__(self, base_url: str, token: Optional[str] = None,")
	fmt.Fprintln(w, "                 http: Optional[httpx.Client] = None):")
	fmt.Fprintln(w, "        self.base_url = base_url.rstrip(\"/\")")
	fmt.Fprintln(w, "        self.token = token")
	fmt.Fprintln(w, "        self._http = http or httpx.Client(timeout=30.0)")
	fmt.Fprintln(w, "        self._lock = threading.Lock()")
	fmt.Fprintln(w, "        self._last_id: Optional[str] = None")
	fmt.Fprintln(w)
	fmt.Fprintln(w, "    @property")
	fmt.Fprintln(w, "    def last_request_id(self) -> Optional[str]:")
	fmt.Fprintln(w, "        with self._lock:")
	fmt.Fprintln(w, "            return self._last_id")
	fmt.Fprintln(w)
	fmt.Fprintln(w, "    def set_token(self, token: Optional[str]) -> None:")
	fmt.Fprintln(w, "        self.token = token")
	fmt.Fprintln(w)
	fmt.Fprintln(w, "    def call(self, service: str, method: str, args: Any = None) -> Any:")
	fmt.Fprintln(w, `        """Generic call. Sends {"args": args} to /rpc/<service>/<method>,`)
	fmt.Fprintln(w, "        parses the envelope, returns the `data` field (or raises SovError).")
	fmt.Fprintln(w, `        """`)
	fmt.Fprintln(w, "        if hasattr(args, \"__dataclass_fields__\"):")
	fmt.Fprintln(w, "            args = asdict(args)")
	fmt.Fprintln(w, "        headers = {\"Content-Type\": \"application/json\"}")
	fmt.Fprintln(w, "        if self.token:")
	fmt.Fprintln(w, "            headers[\"Authorization\"] = f\"Bearer {self.token}\"")
	fmt.Fprintln(w, "        url = f\"{self.base_url}/rpc/{service}/{method}\"")
	fmt.Fprintln(w, "        resp = self._http.post(url, json={\"args\": args}, headers=headers)")
	fmt.Fprintln(w, "        rid = resp.headers.get(\"X-Sov-Request-Id\")")
	fmt.Fprintln(w, "        if rid:")
	fmt.Fprintln(w, "            with self._lock:")
	fmt.Fprintln(w, "                self._last_id = rid")
	fmt.Fprintln(w, "        try:")
	fmt.Fprintln(w, "            env = resp.json()")
	fmt.Fprintln(w, "        except Exception as e:")
	fmt.Fprintln(w, "            raise SovError(\"sov.decode\", f\"non-JSON response: {resp.text!r}\") from e")
	fmt.Fprintln(w, "        err = env.get(\"error\") if isinstance(env, dict) else None")
	fmt.Fprintln(w, "        if err:")
	fmt.Fprintln(w, "            raise SovError(err.get(\"code\", \"\"), err.get(\"message\", \"\"))")
	fmt.Fprintln(w, "        return env.get(\"data\") if isinstance(env, dict) else env")
	fmt.Fprintln(w)
}

func emitTypes(w io.Writer, report *gateway.IntrospectReport) {
	if len(report.Types) == 0 {
		return
	}
	names := make([]string, 0, len(report.Types))
	for n := range report.Types {
		names = append(names, n)
	}
	sort.Strings(names)
	fmt.Fprintln(w, "# ---- Types ------------------------------------------------------------------")
	fmt.Fprintln(w)
	for _, name := range names {
		td := report.Types[name]
		fmt.Fprintln(w, "@dataclass")
		fmt.Fprintf(w, "class %s:\n", pyIdent(name))
		emitClassDoc(w, "    ", td.Name, "", "", "", false)
		if len(td.Fields) == 0 {
			fmt.Fprintln(w, "    pass")
			fmt.Fprintln(w)
			continue
		}
		// Sort fields: required first, optional last (Python rule:
		// non-default args may not follow default args).
		ordered := make([]rpc.ParamField, 0, len(td.Fields))
		var required, optional []rpc.ParamField
		for _, f := range td.Fields {
			if f.Omitempty || !f.Required {
				optional = append(optional, f)
			} else {
				required = append(required, f)
			}
		}
		ordered = append(ordered, required...)
		ordered = append(ordered, optional...)
		for _, f := range ordered {
			emitFieldDoc(w, "    ", f)
			typ := pyTypeOf(f)
			if f.Omitempty || !f.Required {
				fmt.Fprintf(w, "    %s: Optional[%s] = None\n", pyFieldIdent(f.JSONName), typ)
			} else {
				fmt.Fprintf(w, "    %s: %s\n", pyFieldIdent(f.JSONName), typ)
			}
		}
		fmt.Fprintln(w)
	}
}

func emitRouters(w io.Writer, report *gateway.IntrospectReport) {
	if len(report.Services) == 0 {
		return
	}
	services := make([]string, 0, len(report.Services))
	for s := range report.Services {
		services = append(services, s)
	}
	sort.Strings(services)
	fmt.Fprintln(w, "# ---- Routers ----------------------------------------------------------------")
	fmt.Fprintln(w)
	for _, svc := range services {
		for _, rd := range report.Services[svc] {
			emitRouter(w, rd)
		}
	}
	// App convenience.
	fmt.Fprintln(w, "class App(SovClient):")
	fmt.Fprintln(w, `    """Convenience subclass exposing each router as a member."""`)
	fmt.Fprintln(w, "    def __init__(self, base_url: str, token: Optional[str] = None,")
	fmt.Fprintln(w, "                 http: Optional[httpx.Client] = None):")
	fmt.Fprintln(w, "        super().__init__(base_url, token=token, http=http)")
	for _, svc := range services {
		for _, rd := range report.Services[svc] {
			fmt.Fprintf(w, "        self.%s = %s(self)\n", pyFieldIdent(rd.Router), pyIdent(rd.Router))
		}
	}
}

func emitRouter(w io.Writer, rd rpc.RouterDescriptor) {
	fmt.Fprintf(w, "class %s:\n", pyIdent(rd.Router))
	fmt.Fprintf(w, "    \"\"\"%s router.\"\"\"\n", rd.Router)
	fmt.Fprintln(w, "    def __init__(self, client: SovClient):")
	fmt.Fprintln(w, "        self._c = client")
	for _, md := range rd.Methods {
		emitMethod(w, rd.Router, md)
	}
	fmt.Fprintln(w)
}

func emitMethod(w io.Writer, router string, md rpc.MethodDescriptor) {
	resp := responseTypeName(md)
	respHint := "Any"
	if resp != "" {
		respHint = resp
	}
	paramType := pyIdent(router) + strs.Capitalize(md.Method) + "Params"
	if md.HasParams {
		fmt.Fprintf(w, "    def %s(self, p: %s) -> %s:\n", pyMethodIdent(md.Method), paramType, respHint)
		fmt.Fprintf(w, "        return self._c.call(%q, %q, p)\n", router, md.Method)
	} else {
		fmt.Fprintf(w, "    def %s(self) -> %s:\n", pyMethodIdent(md.Method), respHint)
		fmt.Fprintf(w, "        return self._c.call(%q, %q)\n", router, md.Method)
	}
}

func emitClassDoc(w io.Writer, indent, title, desc, doc, example string, deprecated bool) {
	if title == "" && desc == "" && doc == "" && example == "" && !deprecated {
		return
	}
	fmt.Fprintf(w, "%s\"\"\"", indent)
	first := true
	flush := func(line string) {
		if first {
			fmt.Fprintln(w, line)
			first = false
		} else {
			fmt.Fprintf(w, "%s%s\n", indent, line)
		}
	}
	if title != "" {
		if desc != "" {
			flush(fmt.Sprintf("%s — %s", title, desc))
		} else {
			flush(title)
		}
	} else if desc != "" {
		flush(desc)
	}
	if doc != "" {
		for _, line := range strings.Split(doc, "\n") {
			flush(line)
		}
	}
	if example != "" {
		flush("Example: " + example)
	}
	if deprecated {
		flush("DEPRECATED.")
	}
	fmt.Fprintf(w, "%s\"\"\"\n", indent)
}

func emitFieldDoc(w io.Writer, indent string, f rpc.ParamField) {
	// Python doesn't have inline field docstrings the way Kotlin/Swift
	// do; emit a hash comment instead.
	if f.Title == "" && f.Desc == "" && f.Doc == "" && f.Example == "" && !f.Deprecated {
		return
	}
	if f.Title != "" {
		if f.Desc != "" {
			fmt.Fprintf(w, "%s# %s — %s\n", indent, f.Title, f.Desc)
		} else {
			fmt.Fprintf(w, "%s# %s\n", indent, f.Title)
		}
	} else if f.Desc != "" {
		fmt.Fprintf(w, "%s# %s\n", indent, f.Desc)
	}
	if f.Doc != "" {
		for _, line := range strings.Split(f.Doc, "\n") {
			fmt.Fprintf(w, "%s# %s\n", indent, line)
		}
	}
	if f.Example != "" {
		fmt.Fprintf(w, "%s# example: %s\n", indent, f.Example)
	}
	if f.Deprecated {
		fmt.Fprintf(w, "%s# DEPRECATED\n", indent)
	}
}

func pyTypeOf(f rpc.ParamField) string {
	switch f.SchemaType {
	case "string":
		return "str"
	case "number":
		return "float"
	case "integer":
		return "int"
	case "boolean":
		return "bool"
	case "array":
		return "List[Any]"
	case "object":
		if f.TypeName != "" {
			return pyIdent(f.TypeName)
		}
		return "Dict[str, Any]"
	default:
		return "Any"
	}
}

func responseTypeName(md rpc.MethodDescriptor) string {
	s := strings.TrimSpace(md.ResponseTypeScript)
	if s == "" || s == "void" || s == "unknown" {
		return ""
	}
	if !strs.IsIdent(s) {
		return ""
	}
	return pyIdent(s)
}

func pyIdent(name string) string {
	out := strings.ReplaceAll(name, ".", "")
	out = strings.ReplaceAll(out, "-", "_")
	if out == "" {
		return out
	}
	r := []rune(out)
	if r[0] >= 'a' && r[0] <= 'z' {
		r[0] -= 'a' - 'A'
	}
	return string(r)
}

func pyFieldIdent(name string) string {
	// camelCase JSON keys are valid Python identifiers; just guard
	// against keyword collisions.
	if isPythonKeyword(name) {
		return name + "_"
	}
	return name
}

func pyMethodIdent(name string) string {
	if isPythonKeyword(name) {
		return name + "_"
	}
	return name
}

func isPythonKeyword(s string) bool {
	switch s {
	case "False", "None", "True", "and", "as", "assert", "async", "await",
		"break", "class", "continue", "def", "del", "elif", "else", "except",
		"finally", "for", "from", "global", "if", "import", "in", "is",
		"lambda", "nonlocal", "not", "or", "pass", "raise", "return", "try",
		"while", "with", "yield":
		return true
	}
	return false
}
