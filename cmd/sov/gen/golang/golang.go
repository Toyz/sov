// Package golang implements the `sovgen go` subcommand. Hits a
// running gateway's /rpc/_introspect and emits a single Go file:
// package decl + one struct per named type + one interface per
// router with one typed method per RPC + one client constructor
// per router that wraps gateway.NewClient.
//
// Consumers import the generated package and program against the
// interface — same shape whether the implementing handler is local
// or remote (PEMM).
package golang

import (
	"flag"
	"fmt"
	"io"
	"sort"
	"strings"
	"unicode"

	"github.com/Toyz/sov/cmd/sov/internal/catalog"
	"github.com/Toyz/sov/cmd/sov/internal/strs"
	"github.com/Toyz/sov/gateway"
	"github.com/Toyz/sov/rpc"
)

// Run executes the go subcommand. Thin wrapper over catalog.RunEmitter;
// the go-specific extras are the --with-http-caller flag (registered via
// ExtraFlags and read into the Emit closure) and Emit's three-arg shape.
func Run(argv []string, stdout, stderr io.Writer) int {
	var withHTTP *bool
	return catalog.RunEmitter(catalog.EmitterSpec{
		Name:           "sovgen go",
		DefaultOut:     "./client.go",
		OutHelp:        "output .go file; pass \"-\" for stdout",
		DefaultPackage: "sovclient",
		PackageHelp:    "Go package name for the generated file",
		ExtraFlags: func(fs *flag.FlagSet) func() {
			withHTTP = fs.Bool("with-http-caller", false, "also emit a default HTTPCaller backed by net/http (exposes server-stamped X-Sov-Request-Id via LastRequestID)")
			return nil
		},
		Emit: func(w io.Writer, pkg string, report *gateway.IntrospectReport) {
			Emit(w, pkg, report, *withHTTP)
		},
	}, argv, stdout, stderr)
}

// Emit writes the Go contract package for report into w under package
// pkg. When withHTTP is true the file also bundles a default net/http
// Caller. Exposed so `sov gen all` can drive every emitter directly.
func Emit(w io.Writer, pkg string, report *gateway.IntrospectReport, withHTTP bool) {
	fmt.Fprintln(w, "// Code generated by sovgen — DO NOT EDIT.")
	fmt.Fprintln(w, "// Source: aggregated /rpc/_introspect catalog.")
	fmt.Fprintln(w)
	fmt.Fprintf(w, "package %s\n\n", pkg)
	if withHTTP {
		fmt.Fprintln(w, `import (`)
		fmt.Fprintln(w, `	"bytes"`)
		fmt.Fprintln(w, `	"context"`)
		fmt.Fprintln(w, `	"encoding/json"`)
		fmt.Fprintln(w, `	"fmt"`)
		fmt.Fprintln(w, `	"io"`)
		fmt.Fprintln(w, `	"net/http"`)
		fmt.Fprintln(w, `	"strings"`)
		fmt.Fprintln(w, `	"sync"`)
		fmt.Fprintln(w, `	"time"`)
		fmt.Fprintln(w, `)`)
	} else {
		fmt.Fprintln(w, `import "context"`)
	}
	fmt.Fprintln(w)
	fmt.Fprintln(w, `// Caller is the transport abstraction every generated client wraps.`)
	fmt.Fprintln(w, `// Consumers plug in their own (typically a sov.Client backed by HTTP or`)
	fmt.Fprintln(w, `// an in-process LocalClient) — the generated client stays PEMM-pure:`)
	fmt.Fprintln(w, `// same shape whether the handler is local or remote.`)
	fmt.Fprintln(w, `type Caller interface {`)
	fmt.Fprintln(w, `	Call(ctx context.Context, service, method string, args, out any) error`)
	fmt.Fprintln(w, "}")
	fmt.Fprintln(w)

	// Router interface names — a generated TYPE that collides with one
	// gets a "Model" suffix (see goTypeIdent). A Go file can't declare both
	// `type Page interface` (router) and `type Page struct` (model), so the
	// model yields the name to the router and becomes PageModel.
	routers := map[string]bool{}
	for svc := range report.Services {
		for _, rd := range report.Services[svc] {
			routers[goIdent(rd.Router)] = true
		}
	}

	emitTypes(w, report, routers)
	emitInterfaces(w, report)
	if withHTTP {
		emitHTTPCaller(w)
	}
}

// emitHTTPCaller emits a default net/http-backed Caller. Pure
// transport — no request-id stamping (the SERVER mints those; a
// client surfaces them by reading the X-Sov-Request-Id response
// header via LastRequestID).
func emitHTTPCaller(w io.Writer) {
	fmt.Fprintln(w, "// ---- HTTPCaller (default net/http transport) -------------------------------")
	fmt.Fprintln(w)
	fmt.Fprintln(w, `// HTTPCaller is the default Caller backed by net/http POSTing to`)
	fmt.Fprintln(w, `// /rpc/<service>/<method> on BaseURL. Pure client transport — the`)
	fmt.Fprintln(w, `// SERVER mints X-Sov-Request-Id; HTTPCaller surfaces the most-recent`)
	fmt.Fprintln(w, `// id via LastRequestID() so callers can log it for correlation.`)
	fmt.Fprintln(w, `type HTTPCaller struct {`)
	fmt.Fprintln(w, `	BaseURL string`)
	fmt.Fprintln(w, `	HTTP    *http.Client`)
	fmt.Fprintln(w, `	// Headers are sent on every request (Authorization etc).`)
	fmt.Fprintln(w, `	Headers http.Header`)
	fmt.Fprintln(w, `	mu      sync.RWMutex`)
	fmt.Fprintln(w, `	lastID  string`)
	fmt.Fprintln(w, `}`)
	fmt.Fprintln(w)
	fmt.Fprintln(w, `// NewHTTPCaller returns an HTTPCaller with a 30s default timeout.`)
	fmt.Fprintln(w, `func NewHTTPCaller(baseURL string) *HTTPCaller {`)
	fmt.Fprintln(w, `	return &HTTPCaller{`)
	fmt.Fprintln(w, `		BaseURL: baseURL,`)
	fmt.Fprintln(w, `		HTTP:    &http.Client{Timeout: 30 * time.Second},`)
	fmt.Fprintln(w, `	}`)
	fmt.Fprintln(w, `}`)
	fmt.Fprintln(w)
	fmt.Fprintln(w, `// LastRequestID returns the X-Sov-Request-Id from the most recent`)
	fmt.Fprintln(w, `// response (empty when the server hasn't responded yet or didn't`)
	fmt.Fprintln(w, `// stamp one). Use for log correlation.`)
	fmt.Fprintln(w, `func (c *HTTPCaller) LastRequestID() string {`)
	fmt.Fprintln(w, `	c.mu.RLock()`)
	fmt.Fprintln(w, `	defer c.mu.RUnlock()`)
	fmt.Fprintln(w, `	return c.lastID`)
	fmt.Fprintln(w, `}`)
	fmt.Fprintln(w)
	fmt.Fprintln(w, `// Call implements Caller.`)
	fmt.Fprintln(w, `func (c *HTTPCaller) Call(ctx context.Context, service, method string, args, out any) error {`)
	fmt.Fprintln(w, `	wrap := map[string]any{"args": args}`)
	fmt.Fprintln(w, `	if args == nil {`)
	fmt.Fprintln(w, `		wrap["args"] = []any{}`)
	fmt.Fprintln(w, `	}`)
	fmt.Fprintln(w, `	body, err := json.Marshal(wrap)`)
	fmt.Fprintln(w, `	if err != nil {`)
	fmt.Fprintln(w, `		return fmt.Errorf("marshal args: %w", err)`)
	fmt.Fprintln(w, `	}`)
	fmt.Fprintln(w, `	url := strings.TrimRight(c.BaseURL, "/") + "/rpc/" + service + "/" + method`)
	fmt.Fprintln(w, `	req, err := http.NewRequestWithContext(ctx, http.MethodPost, url, bytes.NewReader(body))`)
	fmt.Fprintln(w, `	if err != nil {`)
	fmt.Fprintln(w, `		return err`)
	fmt.Fprintln(w, `	}`)
	fmt.Fprintln(w, `	req.Header.Set("Content-Type", "application/json")`)
	fmt.Fprintln(w, `	for k, vs := range c.Headers {`)
	fmt.Fprintln(w, `		for _, v := range vs {`)
	fmt.Fprintln(w, `			req.Header.Add(k, v)`)
	fmt.Fprintln(w, `		}`)
	fmt.Fprintln(w, `	}`)
	fmt.Fprintln(w, `	resp, err := c.HTTP.Do(req)`)
	fmt.Fprintln(w, `	if err != nil {`)
	fmt.Fprintln(w, `		return err`)
	fmt.Fprintln(w, `	}`)
	fmt.Fprintln(w, `	defer resp.Body.Close()`)
	fmt.Fprintln(w, `	if id := resp.Header.Get("X-Sov-Request-Id"); id != "" {`)
	fmt.Fprintln(w, `		c.mu.Lock()`)
	fmt.Fprintln(w, `		c.lastID = id`)
	fmt.Fprintln(w, `		c.mu.Unlock()`)
	fmt.Fprintln(w, `	}`)
	fmt.Fprintln(w, `	raw, err := io.ReadAll(resp.Body)`)
	fmt.Fprintln(w, `	if err != nil {`)
	fmt.Fprintln(w, `		return err`)
	fmt.Fprintln(w, `	}`)
	fmt.Fprintln(w, `	var env struct {`)
	fmt.Fprintln(w, `		Data  json.RawMessage `+"`json:\"data\"`"+``)
	fmt.Fprintln(w, `		Error *struct {`)
	fmt.Fprintln(w, `			Message string `+"`json:\"message\"`"+``)
	fmt.Fprintln(w, `			Code    string `+"`json:\"code\"`"+``)
	fmt.Fprintln(w, `		} `+"`json:\"error\"`"+``)
	fmt.Fprintln(w, `	}`)
	fmt.Fprintln(w, `	if err := json.Unmarshal(raw, &env); err != nil {`)
	io.WriteString(w, "\t\treturn fmt.Errorf(\"decode envelope: %w (body=%s)\", err, string(raw))\n")
	fmt.Fprintln(w, `	}`)
	fmt.Fprintln(w, `	if env.Error != nil {`)
	io.WriteString(w, "\t\treturn fmt.Errorf(\"rpc %s.%s: %s (%s)\", service, method, env.Error.Message, env.Error.Code)\n")
	fmt.Fprintln(w, `	}`)
	fmt.Fprintln(w, `	if out != nil && len(env.Data) > 0 {`)
	fmt.Fprintln(w, `		return json.Unmarshal(env.Data, out)`)
	fmt.Fprintln(w, `	}`)
	fmt.Fprintln(w, `	return nil`)
	fmt.Fprintln(w, `}`)
}

func emitTypes(w io.Writer, report *gateway.IntrospectReport, routers map[string]bool) {
	if len(report.Types) == 0 {
		return
	}
	names := make([]string, 0, len(report.Types))
	for n := range report.Types {
		names = append(names, n)
	}
	sort.Strings(names)
	fmt.Fprintln(w, "// ---- Types ----------------------------------------------------------------")
	fmt.Fprintln(w)
	for _, name := range names {
		td := report.Types[name]
		fmt.Fprintf(w, "type %s struct {\n", goTypeIdent(name, routers))
		for _, f := range td.Fields {
			tag := f.JSONName
			if f.Omitempty {
				tag += ",omitempty"
			}
			fmt.Fprintf(w, "\t%s %s `json:\"%s\"`\n", strs.Capitalize(f.JSONName), goTypeOf(f, routers), tag)
		}
		fmt.Fprintln(w, "}")
		fmt.Fprintln(w)
	}
}

func emitInterfaces(w io.Writer, report *gateway.IntrospectReport) {
	if len(report.Services) == 0 {
		return
	}
	services := make([]string, 0, len(report.Services))
	for s := range report.Services {
		services = append(services, s)
	}
	sort.Strings(services)
	fmt.Fprintln(w, "// ---- Services -------------------------------------------------------------")
	fmt.Fprintln(w)
	for _, svc := range services {
		for _, rd := range report.Services[svc] {
			emitInterface(w, &rd)
			emitClient(w, &rd)
		}
	}
}

func emitInterface(w io.Writer, rd *rpc.RouterDescriptor) {
	fmt.Fprintf(w, "// %s is the typed contract for the %s service. Same shape whether\n", goIdent(rd.Router), rd.Router)
	fmt.Fprintln(w, "// the underlying handler runs in-process or out across the mesh.")
	fmt.Fprintf(w, "type %s interface {\n", goIdent(rd.Router))
	for _, md := range rd.Methods {
		fmt.Fprintf(w, "\t%s(ctx context.Context%s) %s\n", strs.Capitalize(md.Method), paramsArg(rd.Router, md), returnTypes(md))
	}
	fmt.Fprintln(w, "}")
	fmt.Fprintln(w)
}

func emitClient(w io.Writer, rd *rpc.RouterDescriptor) {
	typeName := goIdent(rd.Router) + "Client"
	fmt.Fprintf(w, "// %s wraps a Caller and implements %s.\n", typeName, goIdent(rd.Router))
	fmt.Fprintf(w, "type %s struct{ c Caller }\n\n", typeName)
	fmt.Fprintf(w, "// New%s returns a typed client backed by c.\n", typeName)
	fmt.Fprintf(w, "func New%s(c Caller) *%s { return &%s{c: c} }\n\n", typeName, typeName, typeName)
	for _, md := range rd.Methods {
		emitClientMethod(w, typeName, rd.Router, &md)
	}
}

func emitClientMethod(w io.Writer, typeName, service string, md *rpc.MethodDescriptor) {
	resultType := responseTypeName(md)
	hasResult := resultType != ""
	fmt.Fprintf(w, "func (c *%s) %s(ctx context.Context%s) %s {\n",
		typeName, strs.Capitalize(md.Method), paramsArg(service, *md), returnTypes(*md))
	hasParams := methodHasParams(*md)
	switch {
	case hasParams && hasResult:
		fmt.Fprintf(w, "\tvar out %s\n", resultType)
		fmt.Fprintf(w, "\tif err := c.c.Call(ctx, %q, %q, p, &out); err != nil {\n", service, md.Method)
		fmt.Fprintln(w, "\t\treturn nil, err")
		fmt.Fprintln(w, "\t}")
		fmt.Fprintln(w, "\treturn &out, nil")
	case hasParams:
		fmt.Fprintf(w, "\treturn c.c.Call(ctx, %q, %q, p, nil)\n", service, md.Method)
	case hasResult:
		fmt.Fprintf(w, "\tvar out %s\n", resultType)
		fmt.Fprintf(w, "\tif err := c.c.Call(ctx, %q, %q, nil, &out); err != nil {\n", service, md.Method)
		fmt.Fprintln(w, "\t\treturn nil, err")
		fmt.Fprintln(w, "\t}")
		fmt.Fprintln(w, "\treturn &out, nil")
	default:
		fmt.Fprintf(w, "\treturn c.c.Call(ctx, %q, %q, nil, nil)\n", service, md.Method)
	}
	fmt.Fprintln(w, "}")
	fmt.Fprintln(w)
}

// methodHasParams reports whether the client must send a params object.
// Like the ts generator it double-checks len(Params) > 0 rather than
// trusting HasParams alone: a polyglot/hand-written catalog that reports
// HasParams=true but ships zero param fields would otherwise make the
// generator reference a {Router}{Method}Params type that emitTypes never
// emits — a dangling reference that won't compile. Zero fields → no arg.
func methodHasParams(md rpc.MethodDescriptor) bool {
	return md.HasParams && len(md.Params) > 0
}

func paramsArg(router string, md rpc.MethodDescriptor) string {
	if !methodHasParams(md) {
		return ""
	}
	return fmt.Sprintf(", p *%s%sParams", goIdent(router), strs.Capitalize(md.Method))
}

func returnTypes(md rpc.MethodDescriptor) string {
	resultType := responseTypeName(&md)
	if resultType == "" {
		return "error"
	}
	return fmt.Sprintf("(*%s, error)", resultType)
}

// responseTypeName picks a stable Go type name for a method's
// response. When the response is a known named type, returns that.
// Otherwise returns "" (no typed result; method just returns error).
func responseTypeName(md *rpc.MethodDescriptor) string {
	if md.ResponseTypeScript == "" || md.ResponseTypeScript == "void" || md.ResponseTypeScript == "unknown" {
		return ""
	}
	// Best-effort: if the TS shape is a single identifier, use it.
	// Otherwise return "" and let the method return only error.
	ts := strings.TrimSpace(md.ResponseTypeScript)
	if strs.IsIdent(ts) {
		return ts
	}
	return ""
}

func goIdent(s string) string {
	s = strings.ReplaceAll(s, ".", "")
	s = strings.ReplaceAll(s, "-", "_")
	if s == "" {
		return ""
	}
	r := []rune(s)
	r[0] = unicode.ToUpper(r[0])
	return string(r)
}

// goTypeIdent maps a catalog TYPE name to its Go identifier, suffixing
// "Model" when it collides with a router interface name. Without this a
// model struct and a router interface sharing a name (e.g. a `Page` model
// + a `Page` router) would both try to declare `type Page` in one file —
// a compile error. The router keeps the bare name; the model yields.
func goTypeIdent(name string, routers map[string]bool) string {
	id := goIdent(name)
	if routers[id] {
		return id + "Model"
	}
	return id
}

func goTypeOf(f rpc.ParamField, routers map[string]bool) string {
	t := goBaseTypeOf(f, routers)
	// Nullable (Go source field is a pointer): emit a pointer so absence
	// round-trips, keeping omitempty for the wire. Slices and maps are
	// already nil-able reference types — don't double-pointer them.
	if f.Nullable && !strings.HasPrefix(t, "[]") && !strings.HasPrefix(t, "map[") && !strings.HasPrefix(t, "*") {
		t = "*" + t
	}
	return t
}

func goBaseTypeOf(f rpc.ParamField, routers map[string]bool) string {
	switch f.SchemaType {
	case "string":
		return "string"
	case "number":
		return "float64"
	case "integer":
		return "int64"
	case "boolean":
		return "bool"
	case "array":
		return "[]" + goArrayElem(f, routers)
	case "object":
		if f.TypeName != "" {
			return "*" + goTypeIdent(f.TypeName, routers)
		}
		return "map[string]any"
	default:
		return "any"
	}
}

// goArrayElem returns the Go type of a slice element. A named struct
// element (TypeName set) → a pointer to the (collision-aware) type ident,
// mirroring how object fields render; otherwise the element's scalar
// schema (ElemType) maps to the same scalar Go types goBaseTypeOf uses;
// anything else → any.
func goArrayElem(f rpc.ParamField, routers map[string]bool) string {
	if f.TypeName != "" {
		return "*" + goTypeIdent(f.TypeName, routers)
	}
	switch f.ElemType {
	case "string":
		return "string"
	case "number":
		return "float64"
	case "integer":
		return "int64"
	case "boolean":
		return "bool"
	case "object":
		return "map[string]any"
	default:
		return "any"
	}
}
